#include "JoystickDriver.c"
#include "robot.h"

#define PWR_SCAN 50

int start_on_right;
bool ramp;
bool block;
bool wait;
int button;
int pwr_x = 0;
int pwr_y = 0;
int ramp_dir;

void getButton() { //gets next button press
	while (true) {
		button = nNxtButtonPressed;
		if (button != -1)
			break;
	}
	PlaySoundFile("! Click.rso");
	while(nNxtButtonPressed != -1) {
	}
}

int checkForBasket() {
	//returns number of basket if in basket zone
	// returns 0 inbetween zones
	//returns -1 outside of thing
	int dist = nMotorEncoder[M_DRIVE_FL];
	if (dist < 0)
		return -1;
	if (dist < 100) //start of first basket
		return 0;
	if (dist < 200) // end of first baskets
		return 1;
	if (dist < 300) //start of second basket
		return 0;
	if (dist < 400) //end of scond basket
		return 2;
	if (dist < 500) //start of third basket
		return 0;
	if (dist < 600) // end of third basket
		return 3;
	if (dist < 700) //start of fourth basket
		return 0;
	if (dist < 800) //end of fourth basket
		return 4;
	return -1;
}


void trackWall(int dist) {
	int err = dist - SensorValue[SONAR];
	if (abs(err) > 50)
		pwr_y = 0; //ignore if error is really big
	else
		pwr_y = -3*err; //try to correct for error
}

void initializeRobot() {
	servo[SV_AUTO] = 0;
	servo[SV_LID] = 215;
}
task main()
{
	initializeRobot();
	nNxtButtonTask  = -2; //hijacks button control
	disableDiagnosticsDisplay();

	eraseDisplay();
	nxtDisplayCenteredTextLine(0, "Which side?");
	nxtDisplayCenteredTextLine(7, "Left     Right");
	getButton();

	switch (button) {
	case kRightButton:
		start_on_right = 1;
		break;
	case kLeftButton:
		start_on_right = -1;
		break;
	default:
		StopAllTasks();
	}

	eraseDisplay();
	nxtDisplayCenteredTextLine(0, "Block, ramp, or both?");
	nxtDisplayCenteredTextLine(7, "Block/Both/Ramp");
	getButton();

	switch (button) {
	case kRightButton:
		ramp = true;
		block = false;
		break;
	case kLeftButton:
		ramp = false;
		block = true;
		break;
	case kEnterButton:
		ramp = true;
		block = true;
		break;
	default:
		StopAllTasks();
	}

	eraseDisplay();
	nxtDisplayCenteredTextLine(0, "Wait for Start?");
	nxtDisplayCenteredTextLine(7, "Yes    No");
	getButton();

	switch (button) {
	case kRightButton:
		wait = false;
		break;
	case kLeftButton:
		wait = true;
		break;
	default:
		StopAllTasks();
	}

	//optional delay before starting autonomous
	eraseDisplay();
	nxtDisplayCenteredTextLine(0, "Delay?");
	nxtDisplayCenteredBigTextLine(6,"0");

	int delay = 0; //number of seconds to delay
	bool stay = true;
	while(stay) {
		nxtDisplayCenteredBigTextLine(6," %d ",delay);
		getButton();
		switch (button) {
		case kRightButton:
			delay++;
			break;
		case kLeftButton:
			if (delay > 0)
				delay--;
			break;
		case kEnterButton:
			stay = false;
			break;
		default:
			StopAllTasks();
		}
	}

	eraseDisplay();
	nxtDisplayCenteredBigTextLine(0, "Ready?");
	nxtDisplayCenteredBigTextLine(6, "Go!");
	getButton();

	switch (button) {
	case kEnterButton:
		break;
	default:
		StopAllTasks();
	}

	////////////////////////////////////////////////////
	//code that actually runs during match starts here//
	////////////////////////////////////////////////////

	eraseDisplay();
	StartTask(displayDiagnostics);
	wait1Msec(50);

	if (wait)
		waitForStart();

	wait1Msec(delay*1000);
	ClearTimer(T1); //keeps track of autonomous period, 30000 ms long

	if (ramp && !block) {
		//just get on ramp
		goForward(100);
		wait1Msec(900);
		driveFast(-start_on_right*100,0);
		wait1Msec(2100);
		driveStop();
	}

	else {
		//score block in IR goal
		pwr_x = -start_on_right*PWR_SCAN;
		driveTilted(pwr_x, pwr_y);
		while (SensorValue[SONAR] > 100) {
			//wait for it to reach the wall
		}
		nMotorEncoder[M_DRIVE_FL] = 0;
		while (-nMotorEncoder[M_DRIVE_FL] < 6500) {
			nxtDisplayString(3, "%8d     ", nMotorEncoder[M_DRIVE_FL]);

			//int zone = checkForBasket();
			int zone = 1;

			if (SensorValue[IR] == 5 && zone > 0) { //we see the IR beacon, and are in a basket zone
				driveStop();
				nxtDisplayString(3, "%8d     ", nMotorEncoder[M_DRIVE_FL]);
				if (abs(nMotorEncoder[M_DRIVE_FL]) > 2000)
					ramp_dir = -1;
				else
					ramp_dir = 1;
				pwr_x = 0;
				pwr_y = 50;
				while(SensorValue[SONAR] > 35) {
					int ir = SensorValue[IR];
					switch (ir) {
					case 4:
						pwr_x = -10;
						pwr_y = 25;
						break;
					case 6:
						pwr_x = 10;
						pwr_y = 25;
						break;
					default:
						pwr_x = 0;
						pwr_y = 50;
						break;
					}
					driveTilted(pwr_x,pwr_y);
				}
				driveStop();

				servo[SV_AUTO] = 200; //score the autonomous block
				wait1Msec(500);
				servo[SV_AUTO] = 0; //reset so it doesn't get in the way
				break;
			}
			trackWall(40);
			driveTilted(pwr_x, pwr_y);
		}
		driveStop();


		if (ramp) {
			//go remaining distance TODO: return on closest side

			pwr_x = ramp_dir*start_on_right*1.5*PWR_SCAN;
			pwr_y = -10;
			driveTilted(pwr_x, pwr_y);
			while(SensorValue[SONAR] < 200) {
				trackWall(45);
				driveTilted(pwr_x, pwr_y);
			}
			driveStop();

			//get on ramp
			wait1Msec(3000);
			if (ramp_dir == -1)
				goLeft(100);
			else
				goForward(100);
			wait1Msec(1000);
			driveStop();
			wait1Msec(1000);
			goForwardLeft(100);
			wait1Msec(1000);
			driveStop();
			wait1Msec(1000);
			if (ramp_dir == -1)
				motor[M_DRIVE_BL] = 100;
			else
				rotate(-100);
			wait1Msec(200);
			driveStop();
			wait1Msec(100);
			goLeft(100);

			wait1Msec(2000);
			driveStop();
		}
	}
}
