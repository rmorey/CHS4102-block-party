#define PWR_BUCKET 100
#define PWR_DRIVE 100
#define PWR_ROTATE 100
#define SRV_HOOK 30

int drive_direction = 1; //1 for forwards, -1 for backwards
bool tilted_drive = false;
bool lid_closed = false;
int pwr_FR = 0;
int pwr_FL = 0;
int pwr_BR = 0;
int pwr_BL = 0;
int pwr_bucket = 0;


void InitializeRobot (){
	nMotorEncoder[M_slider_R] = 0;
	servo[S_lid] = 250;
	return;
}

task DriveUpdate() {
	//Drive control and bucket control update loop
	while (true) {
		getJoystickSettings(joystick);

		if (tilted_drive) { //titled drive mode, the flag spinner is onsidered the front of the robot
			pwr_FR = (PWR_DRIVE * JS_DRIVE_Y) / 127.0 - PWR_ROTATE*JS_ROTATE/127.0;
			pwr_FL = (PWR_DRIVE * JS_DRIVE_X) / 127.0 + PWR_ROTATE*JS_ROTATE/127.0;
			pwr_BR = (PWR_DRIVE * JS_DRIVE_X) / 127.0 - PWR_ROTATE*JS_ROTATE/127.0;
			pwr_BL = (PWR_DRIVE * JS_DRIVE_Y) / 127.0 + PWR_ROTATE*JS_ROTATE/127.0;
		}

		else {	//standard omniwheel drive

			//pwr_FR = drive_direction*.557*(JS_DRIVE_Y - JS_DRIVE_X - JS_ROTATE);
			//pwr_FL = drive_direction*.557*(JS_DRIVE_Y + JS_DRIVE_X + JS_ROTATE);
			//pwr_BR = drive_direction*.557*(JS_DRIVE_Y + JS_DRIVE_X - JS_ROTATE);
			//pwr_BL = drive_direction*.557*(JS_DRIVE_Y - JS_DRIVE_X + JS_ROTATE);

			pwr_FR = drive_direction*(JS_DRIVE_Y - JS_DRIVE_X - drive_direction*JS_ROTATE);
			pwr_FL = drive_direction*(JS_DRIVE_Y + JS_DRIVE_X + drive_direction*JS_ROTATE);
			pwr_BR = drive_direction*(JS_DRIVE_Y + JS_DRIVE_X - drive_direction*JS_ROTATE);
			pwr_BL = drive_direction*(JS_DRIVE_Y - JS_DRIVE_X + drive_direction*JS_ROTATE);
		}

		//slippage control
		if (abs(pwr_FR) < 10) {
			pwr_FR = 0;
		}
		if (abs(pwr_FL) < 10) {
			pwr_FL = 0;
		}
		if (abs(pwr_BR) < 10) {
			pwr_BR = 0;
		}
		if (abs(pwr_BL) < 10) {
			pwr_BL = 0;
		}

		motor[M_drive_FR] = pwr_FR;
		motor[M_drive_FL] = pwr_FL;
		motor[M_drive_BR] = pwr_BR;
		motor[M_drive_BL] = pwr_BL;

		EndTimeSlice();
	}
}

task BucketUpdate() {
	while(true) {
		//bucket control
		int js_bucket = JS_BUCKET;
		if (abs(js_bucket) < 5) js_bucket = 0; //slippage control
			int	enc_R_bucket = nMotorEncoder[M_slider_R];
		nxtDisplayString(0, "%8d", nMotorEncoder[M_slider_R]);
		if (!BTN_ENC_IGNORE && ((enc_R_bucket < 0 && js_bucket < 0) || (enc_R_bucket > ENC_BUCKET_MAX && js_bucket > 0))) {
			pwr_bucket = 0;
		}
		else {
			pwr_bucket = PWR_BUCKET*(js_bucket/127.0);
		}

		motor[M_slider_L] = pwr_bucket;
		motor[M_slider_R] = pwr_bucket;

		EndTimeSlice();
	}
}

task main() {
	InitializeRobot();
	waitForStart();

	StartTask(DriveUpdate);
	StartTask(BucketUpdate);

	while(true) {
		getJoystickSettings(joystick);

		if(bDisconnected) { //kills motors and program if connection is lost
			haltAllMotors();
			StopAllTasks();
		}

		// belt control
		while (BTN_BELT_IN) {
			motor[M_belt] = 100;
			getJoystickSettings(joystick);
		}
		motor[M_belt] = 0;

		while (BTN_BELT_OUT) {
			motor[M_belt] = -100;
			getJoystickSettings(joystick);
		}
		motor[M_belt] = 0;


		//score blocks button
		if (BTN_SCORE) {
			servo[S_lid] = 250;
			lid_closed = false;
			scoreBlocks();

			wait1Msec(500);
		}


		// Global motor kill
		if(BTN_KILL) haltAllMotors();

		//switches to the tilted drive mode, flag spinner is now front of robot
		if (BTN_TILT) tilted_drive = true;

		//backwards drive mode
		if (BTN_BACK) {
			tilted_drive = false;
			drive_direction = -1;
		}

		//revert to standard drive mode
		if (BTN_FRONT) {
			tilted_drive = false;
			drive_direction = 1;
		}


		//flag control hold button to run flag spinner
		while (BTN_FLAG) {
			motor[M_flag] = 100;
			getJoystickSettings(joystick);
		}
		motor[M_flag] = 0;

		//reset bucket encoder to zero
		if (BTN_ENC_RESET) nMotorEncoder[M_slider_R] = 0;

		//hook contrlol
		while (BTN_HOOK_OUT) {
			servo[S_hook] = 64;
			getJoystickSettings(joystick);
		}

		while (BTN_HOOK_IN) {
			servo[S_hook] = 192;
			getJoystickSettings(joystick);
		}
		servo[S_hook] = 128;

		if (BTN_LID) {
			if (lid_closed) {
				servo[S_lid] = 250;
				lid_closed =false;
			}
			else {
				servo[S_lid] = 0;
				lid_closed = true;
			}
			wait1Msec(500);
		}

		EndTimeSlice();
	}
}
